package com.example.chapter15.unit1;

/**
 * 抽象组
 */
public abstract class Group {
  /**
   * 找到组
   */
  public abstract void find();

  /**
   * 增加功能
   */
  public abstract void add();

  /**
   * 删除功能
   */
  public abstract void delete();

  /**
   * 修改功能
   */
  public abstract void change();

  /**
   * 列出所有计划
   */
  public abstract void plan();
}
